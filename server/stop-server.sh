#!/bin/bash
# Скрипт для остановки сервера на порту 3001

echo "Поиск процесса на порту 3001..."
PORT=3001

# Найти PID процесса
PID=$(lsof -ti:$PORT)

if [ -z "$PID" ]; then
    echo "Процесс на порту $PORT не найден"
else
    echo "Найден процесс с PID: $PID"
    echo "Остановка процесса..."
    kill -9 $PID
    echo "Процесс остановлен"
fi

# Альтернативный способ через fuser
# fuser -k 3001/tcp

echo "Готово!"
